export async function seedUiTranslations(
  prisma: PrismaClient,
): Promise<void> {
  for (const [key, zh, en] of uiTranslationCatalog) {
    for (const [languageCode, fieldValue] of [
      ['zh-CN', zh],
      ['en-US', en],
    ] as const) {
      await prisma.translation.upsert({
        where: {
          contentType_contentId_fieldName_languageCode: {
            contentType: 'ui',
            contentId: key,
            fieldName: 'label',
            languageCode,
          },
        },
        create: {
          contentType: 'ui',
          contentId: key,
          fieldName: 'label',
          languageCode,
          fieldValue,
          reviewStatus: 'Approved',
        },
        update: {
          fieldValue,
          reviewStatus: 'Approved',
        },
      });
    }
  }
}
